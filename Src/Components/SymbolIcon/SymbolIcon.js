const FONT_FAMILY = "JyoIconsFont_" + new Date().getTime().toString(36) + "-" + Math.random().toString(36).substr(2, 9);

/**
 * 
 * @extends Basic
 * @class
 */
export default class SymbolIcon extends Basic {
    /**
     * 
     * @returns {Object}
     */
    static get SYMBOL_MAP() {
        return SYMBOL_MAP;
    }

    /**
     * Icons
     * @type {URL}
     */
    static #iconFont = new URL("./Fonts/FluentSystemIcons-Resizable.woff2", ASSETS_PATH).toString();

    /**
     * 
     * @type {FontFace}
     */
    static #loadedFont = null;

    /**
     * 
     * @type {HTMLElement}
     */
    #contentEl = null;

    /**
     * 
     * @type {String}
     */
    #symbol = "";

    /**
     * 
     * @constructor
     * @returns {SymbolIcon} 
     */
    constructor() {
        // 
        super();

        this.DefineProperties({
            Symbol: {
                /**
                 * 
                 * @this {SymbolIcon}
                 * @returns {string} 
                 */
                get() {
                    return this.#symbol;
                },
                set: MethodOverload()
                    .Add([String],
                        /**
                         * 
                         * @this {SymbolIcon}
                         * @param {string} value 
                         */
                        function (value) {
                            this.#symbol = value;
                            const symbol = SYMBOL_MAP[value];
                            if (symbol) {
                                this.#contentEl.textContent = String.fromCodePoint(parseInt(symbol, 16));
                            } else {
                                this.#contentEl.textContent = "";
                            }
                        })
            }
        });
    }

    /**
     * 
     */
    async Load() {
        this.#contentEl = this.Root.querySelector("span");
    }

    static {
        Base.DefineProperties(this, {
            IconFont: {
                /**
                 * 
                 * @this {typeof SymbolIcon}
                 * @returns {URL} 
                 */
                get() {
                    return this.#iconFont;
                },
                set: MethodOverload()
                    .Add([String],
                        /**
                         * 
                         * @this {typeof SymbolIcon}
                         * @param {String} value 
                         */
                        async function (value) {
                            if (this.#loadedFont) {
                                document.fonts.delete(this.#loadedFont);
                                this.#loadedFont = null;
                            }
                            if (!value) return;
                            let font = new FontFace(FONT_FAMILY, `url(${this.#iconFont})`);
                            await font.load();
                            document.fonts.add(font);
                            this.#loadedFont = font;
                        })
            }
        });

        this.IconFont = this.IconFont;

        // 
        Register(this, import.meta.url, {
            template: "<span></span>",
            style: `
                span { font-family: "${FONT_FAMILY}"; color: var(--foreground); }
            `
        });
    }
}
